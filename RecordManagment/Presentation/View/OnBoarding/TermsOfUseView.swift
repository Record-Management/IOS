                Image(systemName: "checkmark.circle.fill")
                    .foregroundStyle(.white, isOpen ? Color.Primary.main() : Color.Gray._400())
                    .padding(.trailing, 8)
                    .onTapGesture {
                        isOpen.toggle()
                    }
                Text("이용약관")
                    .typography(.p14Medium)
                    .foregroundStyle(Color(hex: "#999999"))
                    .padding(.trailing, 4)
                Text("(필수)")
                    .typography(.p14Medium)
                    .foregroundStyle(Color.Primary.main())
                Spacer()
                Image(systemName: "chevron.down")
                    .rotationEffect(Angle(degrees: isScroll ? 0 : 180))
                    .onTapGesture {
                        withAnimation {
                            isScroll.toggle()
                        }
                    }
            }
            .padding(.vertical, 12)
            Spacer().frame(height: 6)
            if isScroll {
                scrollContent
            }
            Spacer()
            Text("확인")
                .frame(maxWidth: .infinity)
                .padding(.vertical, 14)
                .foregroundStyle(isOpen ? .white : Color.Primary.light())
                .background(isOpen ? Color.Primary.main() : Color.Primary.lighter())
                .animation(.easeInOut, value: isOpen)
                .clipShape(.rect(cornerRadius: 8))
                .onTapGesture {
                    if isOpen {
                        coordinator.push(.section)
                    }
                }
        }
        .padding()
    }
}


